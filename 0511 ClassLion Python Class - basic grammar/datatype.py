# 정수 int / 실수 float
num1 = int(input("정수 입력 : "))
num2 = float((input("실수 입력 : ")))
print("덧셈 : ",num1+num2)
print("곱셈 : ",num1*num2)
print("나눗셈(몫) : ",num1//num2) # 몫 // 
print("나눗셈(나머지) : ",num1%num2) # 나머지 %
print("나눗셈(나머지+몫) : ",num1/num2) # 몫과 나머지 /
print("거듭제곲 : ", num1**num2) # 거듭제곱
